"use strict";
// THE NULLISH COALESCING OPERATOR:

/* GENERAL EXPLANATION:

-> THE NULLISH OPERATOR IS USED  IF THERE IS A VALUE WHICH IS ZERO AND WE USE THE TERNARY OPERATOR , SO THE ZERO VALUE IS A FALSY VALUE AND FOR THAT REASON THE RESULT WOULD BE NOT AS EXPECTED.

-> USING NULLISH OPERATOR , THE UNDEFINED AND NULL VALUES ARE TREATED AS FALSY VALUES AND ZERO 0 AND "" EMPTY STRING IS TRUTHY VALUES.

*/

const numOfGuests = 0;

// THE TRUE VALUE IS ZERO , AND ZERO IS A FASLY VALUE SO IT WILL LOG 10.
const guests = numOfGuests ? numOfGuests : 10;
console.log(guests);

// WE USE THE NULLISH OPERATOR:
const guest2 = numOfGuests ?? 10;
console.log(guest2); // 0
