// DESTRUCTURING OF ARRAYS:

/* GENERAL EXPLANATION:

-> DESTRUCTURING IS A CONCEPT AND  FEATURE OF ES6 WHICH WE USED TO BREAK OR UNPACK THE WHOLE DATA STRUCTURE LIKE ARRAY OR OBJECT INTO SMALLER COMPONENTS AND CAN STORE INTO A SIMPLE VARIABLE.
-> IT UNPACK THE ARRAY ELEMENTS OR OBJECT KEY-VALUE INTO SIMPLE VARIABLES.


CODE : 

SIMPLE VALUES STORING:
const arr = [1, 2, 3];

const a = arr[0];
const b = arr[1];
const c = arr[2];
console.log(a, b, c);

USING DESTRUCTURING:
const [x, y, z] = arr;
console.log(x, y, z);

-> X REPRESENTS THE FIRST INDEX([0]) AND THEN Y AND Z.
-> IT WON'T AFFECT THE INITIAL ARRAY.

-> IF WE WANT TO STORE THE THIRD INDEX DIRECTLY AFTER 1ST INDEX.
-> WE CAN DO THAT BY SIMPLY LEAVING COMMAS IN THE SECOND INDEX.
-> LIKE THIS:
            [X , , Z].

-> IF WE WANT TO SWITCH THE 1ST INDEX INTO SECOND AND SECOND INTO FIRST INDEX  LIKE SWITCHING , WE CAN SIMPLY DO THAT WITHOUT DEFINING THE THIRD VARIABLE FOR IT.

-> WE WILL INVERT THE VARIABLES:
[Y,X] = [X,Y] = 2 , 1

-> IF WE HAVE NESTED ARRAY , WE WILL SIMPLY DEFINE OR DESTRUCTURE THE VARIABLES SAME AS NESTED ARRAY AND WE WILL GET ALL THE ELEMENTS OF IT.

const nestedArray = [1, 2, 3, [4, 6]];
console.log(nestedArray);

const [x, y, z, [a, b]] = nestedArray;
console.log(x, y, z, a, b);

-> THERE IS ALSO A CONCEPT OF DEFAULT VALUES IN WHICH WE CAN ASSIGN DEFAULT VALUES TO VARIABLES IN ORDER TO GET RID FORM UNDEFINED:

let p [p,q,r] = [9,4]
console.log(p,q,r) // 9 4 undefined

DEFAULT VALUES:
[p=1 , q = 1 , r = 1] = [9,5]
console.log(p,q,r) // 9 5 1
*/

